package org.yannis.master4j.entity;

public class Field {
    private String type;
    private String name;
    private String size;
    private String comment;
    private String defaultValue;
    private String comparedDefaultValue;
    private boolean isPrimary;

    public Field(){}

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSize() {
        return size;
    }

    public void setSize(String size) {
        this.size = size;
    }

    public String getComment() {
        return comment;
    }

    public void setComment(String comment) {
        this.comment = comment;
    }

    public String getDefaultValue() {
        return defaultValue;
    }

    public void setDefaultValue(String defaultValue) {
        this.defaultValue = defaultValue;
    }

    public String getComparedDefaultValue() {
        return comparedDefaultValue;
    }

    public void setComparedDefaultValue(String comparedDefaultValue) {
        this.comparedDefaultValue = comparedDefaultValue;
    }

    public boolean isPrimary() {
        return isPrimary;
    }

    public void setPrimary(boolean primary) {
        isPrimary = primary;
    }
}
